
public class program20 {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {
			for(int j=5;j>=i;j--) {
				System.out.print("* ");
			}
			System.out.println();
		}
		for(int k=5;k>=1;k--) {
			for(int n=5;n>=k;n--) {
				System.out.print("* ");
			}
			System.out.println();
		}

	}

}
