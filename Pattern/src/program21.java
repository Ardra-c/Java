
public class program21 {

	public static void main(String[] args) {
		for(int i=5;i>=1;i--) {
			for(int j=1;j<=i;j++) {
				System.out.print(j+" ");
			}
			System.out.println();
		}
		for(int k=1;k<=5;k++) {
			for(int n=1;n<=k;n++) {
				System.out.print(n+" ");
			}
			System.out.println();
		}
	}

}
